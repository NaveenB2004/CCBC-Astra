    public void autoDataGrab() {
        Connection con = GravityExplore22.DBConnection.connect();
        try {
            Statement stmt = (Statement) con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT COUNT(first_name) "
                    + "FROM details");
            while (rs.next()) {
                int count = rs.getInt(1);
                jLabel19.setText("" + count);
                ResultSet rs2 = stmt.executeQuery("SELECT * FROM details");
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);
                while (rs2.next()) {
                    Object[] row = {rs2.getString("camp_id"),
                        rs2.getString("first_name") + " " + rs2.getString("last_name")};
                    model.addRow(row);

                }

            }
            con.close();
        } catch (SQLException ex) {
        }
    }

    public void logidpass(String loginid) {
        Connection con = GravityExplore22.DBConnection.connect();
        try {
            Statement stmt = (Statement) con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT last_login FROM login "
                    + "WHERE id='" + loginid + "'");
            while (rs.next()) {
                lastlogin.setText(rs.getString("last_login"));
                String logtime = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date());
                stmt.executeUpdate("UPDATE login SET last_login='" + logtime + "' "
                        + "WHERE id='" + loginid + "'");
            }
            con.close();
        } catch (SQLException ex) {
        }
    }

